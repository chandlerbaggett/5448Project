package spring.formModels;

public class AddMenuItem{
	private int id;
	private int price;
	private string description;

	public int getId(){
		return id;
	}
	
	public void setId(){
		this.id = id;
	}

	public int getPrice(){
		return price;
	}

	public void setPrice(){
		this.price = price;
	}

	public string getDescription(){
		return description;
	}

	public void setDescription(){
		this.description = description;
	}
}
